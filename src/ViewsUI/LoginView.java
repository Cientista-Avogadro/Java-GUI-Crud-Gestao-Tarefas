    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(!txtUsername.getText().isEmpty() || txtPassword.getPassword().length > 0){
            context.Login(txtUsername.getText(), new String(txtPassword.getPassword()));
            this.dispose();
        }else{
            context.ShowAlert("Alerta", "Por favor, preencha todos campos!!!!!!!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

